package claudecode

// This file builds the headless `claude -p` invocation Pine uses to generate
// descriptions for an Ansible repo. Two modes:
//
//   - dry-run: --allowedTools Read only. Claude inspects the repo and PRINTS the
//     descriptions it would write; nothing is modified.
//   - write:   --permission-mode acceptEdits + Read,Edit,Write. Claude writes
//     role descriptions into each role's meta/main.yml (galaxy_info.description)
//     and playbook descriptions into a committed pine.yml sidecar.
//
// There is no non-interactive "propose without writing" permission mode, so the
// dry-run/write split is enforced by which tools are allowed rather than by a
// permission flag.

// SidecarName is the repo-root file Pine reads playbook descriptions back from
// and instructs Claude Code to write playbook descriptions into.
const SidecarName = "pine.yml"

// dryRunPrompt and writePrompt share the same analysis brief; only the final
// instruction (print vs. write) differs, so a reviewer can see exactly what
// each mode does.
const briefPrompt = `You are documenting an Ansible repository so a new user can understand it at a glance.

For every ROLE under roles/ that has no galaxy_info.description in its meta/main.yml (or no meta/main.yml at all), and for every PLAYBOOK (the top-level *.yml files that contain plays, typically under playbooks/ or the repo root), compose ONE concise, factual sentence describing what it does. Base each description ONLY on what the files actually contain — the modules and tasks used, the roles applied, the hosts/groups targeted, files and templates referenced. Do not speculate or invent behaviour. Keep each description under ~160 characters, plain text, no trailing period required.`

const dryRunTail = `

DO NOT modify, create, or delete any files. Instead, PRINT your proposed descriptions as a plain list, one per line, in the form:
  role   <role-name>: <description>
  play   <repo-relative-playbook-path>: <description>
List only the roles and playbooks that are currently MISSING a description. End with a one-line summary count.`

const writeTail = `

Now APPLY the descriptions:
  - For each role missing a description, add or update galaxy_info.description in that role's meta/main.yml (create meta/main.yml with a minimal galaxy_info block if it does not exist). Preserve any existing keys.
  - For each playbook, add or update an entry under a top-level ` + "`descriptions:`" + ` mapping in a file named ` + SidecarName + ` at the repository root, keyed by the playbook's repo-relative path (e.g. "playbooks/main/site.yml"). Create ` + SidecarName + ` if it does not exist, with a leading comment noting it is generated by Pine. Do NOT edit the playbook YAML files themselves.
Only add descriptions that are missing; leave existing ones untouched. When done, print a one-line summary of what you changed.`

// Prompt returns the full task prompt for the describe session.
func Prompt(write bool) string {
	if write {
		return briefPrompt + writeTail
	}
	return briefPrompt + dryRunTail
}

// Args builds the argv (after the binary) for a headless describe session in
// workdir. stream selects newline-delimited JSON events (for live UI) over the
// single-object json result.
//
// Note: we deliberately do NOT pass --bare. --bare skips loading ~/.claude, and
// with it Claude Code accepts only env-based auth (ANTHROPIC_API_KEY / a token),
// reporting "Not logged in" for a normal interactive login. Pine runs under the
// user's own account (it already uses their git/SSH credentials), so honouring
// their stored `claude` login is the friction-free path. The dry-run allows
// only the Read tool, so even a repo's own CLAUDE.md cannot cause writes.
func Args(workdir string, write, stream bool) []string {
	args := []string{
		"-p", Prompt(write),
		"--add-dir", workdir,
	}
	if stream {
		// verbose stream-json emits complete assistant/tool/result events (one
		// JSON object per line). We deliberately omit --include-partial-messages:
		// its per-token text_delta events would fragment the final answer across
		// hundreds of log lines. Progress comes from tool-use events instead, and
		// the full descriptions arrive in the final result event.
		args = append(args, "--output-format", "stream-json", "--verbose")
	} else {
		args = append(args, "--output-format", "json")
	}
	if write {
		args = append(args, "--permission-mode", "acceptEdits",
			"--allowedTools", "Read,Edit,Write")
	} else {
		args = append(args, "--allowedTools", "Read")
	}
	return args
}
